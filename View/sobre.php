        <h1 class="ml-3">A Pastelaria</h1>
          <p class="p-1 pl-2" id="psobre">A ideia de fazer pastéis surgiu de uma piada no Rio Grande do Sul. No ano de 1990, desempregado, 
            tomando chimarrão na frente de casa com um irmão, um senhor que passava de bicicleta vendendo pastéis 
            disse: Por que você não faz o mesmo? Abracei a ideia que, durante um ano, deu certo e vi que era um 
            bom negócio. <br>
            Em 1995, residindo em Salvador/BA, observei que não existia nada no ramo de pastelaria no bairro Boca 
            do Rio, logo pensei: Por que não investir na ideia que uma vez havia dado certo? Surgiu, então, a 
            Pastelaria do Gaúcho, uma das pioneiras em Salvador. <br>
            Em 18 de março de 1997, ela foi registrada. A partir daí, a Pastelaria do Gaúcho não parou mais de 
            crescer. Em 2006, foi destaque no Jornal Correio da Bahia e, no mesmo ano, também foi destaque na 
            Revista Veja Salvador como um dos pontos gastronômicos no ramo de pastelarias. <br>
            Hoje, a Pastelaria do Gaúcho é bem frequentada. Possui um ambiente familiar onde não se vende 
            bebidas alcoólicas, mas se come ótimos pastéis regados por sucos, refrigerantes e uma boa música 
            evangélica.
          </p>        
